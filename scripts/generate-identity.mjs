/**
 * Bundle the agent's constitution (identity/agent/*.md) into a TypeScript
 * module so it's compiled into the build — never read from disk at runtime.
 *
 * This is what makes the agent's identity immutable while it's running: the
 * deployment carries a frozen copy, so a bad edit or a mistaken agent write to
 * the repo can't change the live prompt (and these files sit outside the tool
 * whitelist, so the agent can't touch them at all).
 *
 * Runs via the predev/prebuild hooks in package.json. Re-run manually after
 * editing SOUL.md or DIRECTIVES.md:  node scripts/generate-identity.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { join } from "path";

const root = process.cwd();
const read = (rel) => readFileSync(join(root, rel), "utf-8");

const SOUL_MD = read("identity/agent/SOUL.md");
const DIRECTIVES_MD = read("identity/agent/DIRECTIVES.md");

const out = `// AUTO-GENERATED by scripts/generate-identity.mjs — do NOT edit by hand.
// Source of truth: identity/agent/*.md. Bundled into the build so the agent's
// constitution is immutable at runtime.
export const SOUL_MD = ${JSON.stringify(SOUL_MD)};
export const DIRECTIVES_MD = ${JSON.stringify(DIRECTIVES_MD)};
`;

const target = "src/lib/identity/agent-prompt.generated.ts";
writeFileSync(join(root, target), out, "utf-8");
console.log(`[identity] generated ${target}`);
